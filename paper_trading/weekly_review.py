def main() -> None:
    import argparse

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s  %(levelname)-7s  %(message)s",
        datefmt="%H:%M:%S",
    )

    parser = argparse.ArgumentParser(
        description="MiroFish automated weekly review",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--run-now", action="store_true",
        help="Generate and save the weekly review immediately, then exit",
    )
    parser.add_argument(
        "--week-num", type=int, default=None,
        help="Override week number (default: auto-derived from session start date)",
    )
    parser.add_argument(
        "--session-id", default=None,
        help="Paper trading session ID (default: reads active_session.txt)",
    )
    parser.add_argument(
        "--data-dir", default="data/paper_trading",
        help="Path to paper trading data root",
    )
    args = parser.parse_args()

    # Resolve session
    session_id = args.session_id
    if not session_id:
        active_file = Path(args.data_dir) / "active_session.txt"
        if active_file.exists():
            session_id = active_file.read_text(encoding="utf-8").strip()
        else:
            dirs = sorted(
                [d for d in Path(args.data_dir).iterdir() if d.is_dir()],
                key=lambda d: d.stat().st_mtime,
                reverse=True,
            ) if Path(args.data_dir).exists() else []
            session_id = dirs[0].name if dirs else f"paper_{date.today()}"

    logger.info("Session: %s | Data dir: %s", session_id, args.data_dir)

    reviewer = WeeklyReviewer(session_id=session_id, data_dir=args.data_dir)

    if args.run_now:
        week_num = args.week_num or reviewer._compute_week_num()
        content  = reviewer.generate_review(week_num)
        out_path = reviewer.save_review(content, week_num)
        print(f"\nWeekly review written to: {out_path}")
        print(f"Characters: {len(content):,}")
        print("\n--- Preview (first 500 chars) ---")
        print(content[:500])
    else:
        schedule_weekly_review(session_id=session_id, data_dir=args.data_dir)


if __name__ == "__main__":
    main()
