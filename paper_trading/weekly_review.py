@dataclass
class WeekData:
    """Container for all data collected for the weekly review."""

    session_id: str
    week_num: int
    week_start: date
    week_end: date

    # Portfolio
    portfolio_value_start: float = 0.0
    portfolio_value_end: float = 0.0
    starting_capital: float = 1_000_000.0
    cash_end: float = 0.0

    # Trades
    closed_trades: list[dict] = field(default_factory=list)
    open_positions: list[dict] = field(default_factory=list)

    # Regime / signal
    regime_history: list[dict] = field(default_factory=list)
    mirofish_scores: list[float] = field(default_factory=list)

    # Accuracy
    accuracy_report: dict = field(default_factory=dict)

    # NEPSE index
    nepse_start: Optional[float] = None
    nepse_end: Optional[float] = None

    # Cycle logs
    cycle_logs: list[dict] = field(default_factory=list)

    # ── Computed properties ─────────────────────────────────────────────────

    @property
    def week_return_pct(self) -> float:
        if self.portfolio_value_start <= 0:
            return 0.0
        return (self.portfolio_value_end / self.portfolio_value_start - 1) * 100

    @property
    def cumulative_return_pct(self) -> float:
        if self.starting_capital <= 0:
            return 0.0
        return (self.portfolio_value_end / self.starting_capital - 1) * 100

    @property
    def nepse_week_return_pct(self) -> Optional[float]:
        if self.nepse_start and self.nepse_end and self.nepse_start > 0:
            return round((self.nepse_end / self.nepse_start - 1) * 100, 2)
        return None

    @property
    def alpha_pct(self) -> Optional[float]:
        nepse = self.nepse_week_return_pct
        if nepse is None:
            return None
        return round(self.week_return_pct - nepse, 2)

    @property
    def win_rate_pct(self) -> Optional[float]:
        if not self.closed_trades:
            return None
        wins = sum(1 for t in self.closed_trades if t.get("pnl_pct", 0) > 0)
        return round(wins / len(self.closed_trades) * 100, 1)

    @property
    def max_drawdown_pct(self) -> float:
        """Intra-week max drawdown from equity snapshots."""
        values = [s.get("value", 0) for s in self._week_snapshots if s.get("value")]
        if not values:
            return 0.0
        peak = values[0]
        max_dd = 0.0
        for v in values:
            if v > peak:
                peak = v
            dd = (peak - v) / peak * 100 if peak > 0 else 0.0
            max_dd = max(max_dd, dd)
        return round(max_dd, 2)

    @property
    def cash_utilisation_pct(self) -> float:
        if self.portfolio_value_end <= 0:
            return 0.0
        invested = self.portfolio_value_end - self.cash_end
        return round(invested / self.portfolio_value_end * 100, 1)

    # internal shorthand used by max_drawdown_pct
    _week_snapshots: list[dict] = field(default_factory=list, repr=False)


