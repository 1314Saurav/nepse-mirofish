def schedule_weekly_review(session_id: Optional[str] = None, data_dir: str = "data/paper_trading") -> None:
    """
    Start APScheduler with a cron trigger: every Friday at 12:15 UTC (18:00 NST).
    Blocks until Ctrl+C.
    """
    try:
        from apscheduler.schedulers.blocking import BlockingScheduler
        from apscheduler.triggers.cron import CronTrigger
    except ImportError:
        logger.error("apscheduler not installed — run: pip install apscheduler")
        raise SystemExit(1)

    def _resolve_session() -> str:
        if session_id:
            return session_id
        active_file = Path(data_dir) / "active_session.txt"
        if active_file.exists():
            return active_file.read_text(encoding="utf-8").strip()
        dirs = sorted(
            [d for d in Path(data_dir).iterdir() if d.is_dir()],
            key=lambda d: d.stat().st_mtime,
            reverse=True,
        )
        return dirs[0].name if dirs else f"paper_{date.today()}"

    def _run_review():
        sid = _resolve_session()
        reviewer = WeeklyReviewer(session_id=sid, data_dir=data_dir)
        result = reviewer.run_weekly_review()
        logger.info("Scheduled weekly review complete: %s", result)

    scheduler = BlockingScheduler(timezone="UTC")
    scheduler.add_job(
        _run_review,
        CronTrigger(day_of_week="fri", hour=12, minute=15),
        id="weekly_review",
        name="MiroFish weekly review (Friday 18:00 NST)",
        misfire_grace_time=3600,  # fire up to 1h late if scheduler was down
    )

    logger.info("Weekly review scheduler started. Fires every Friday 12:15 UTC (18:00 NST).")
    print("\n  MiroFish weekly review scheduler running.")
    print("  Fires: Every Friday at 12:15 UTC (18:00 NST)")
    print("  Press Ctrl+C to stop.\n")
    scheduler.start()


