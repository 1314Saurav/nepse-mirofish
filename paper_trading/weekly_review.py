    def _compute_week_num(self) -> int:
        """Derive the current week number from the session's first snapshot."""
        pattern = str(self._base / "snapshot_*.json")
        files = sorted(glob.glob(pattern))
        if not files:
            return 1
        try:
            with open(files[0], encoding="utf-8") as fh:
                first_snap = json.load(fh)
            first_date_str = first_snap.get("date") or str(date.today())
            first_date = date.fromisoformat(str(first_date_str))
            delta_days = (date.today() - first_date).days
            return max(1, (delta_days // 7) + 1)
        except Exception:
            return 1

    def _extract_telegram_summary(self, content: str, week_num: int) -> str:
        """Extract a short Telegram summary from the report."""
        lines = content.split("\n")
        summary_lines: list[str] = []
        in_exec = False
        for line in lines:
            if "### 1. Executive Summary" in line:
                in_exec = True
                continue
            if in_exec:
                if line.startswith("###") or line.startswith("---"):
                    break
                if line.strip():
                    summary_lines.append(line.strip())

        exec_text = " ".join(summary_lines)[:400] if summary_lines else "See attached report."

        return (
            f"📋 *MiroFish Weekly Review — Week {week_num}*\n\n"
            f"{exec_text}\n\n"
            f"_Full report saved to_ `data/processed/paper_trading/weekly_review_week{week_num}.md`"
        )

    def _send_telegram(self, message: str) -> bool:
        """Send message via telegram_bot utility."""
        try:
            from paper_trading.telegram_bot import _load_latest_engine_state  # noqa: F401 — confirms import
            import urllib.request
            bot_token = os.environ.get("TELEGRAM_BOT_TOKEN", "")
            chat_id   = os.environ.get("TELEGRAM_CHAT_ID", "")
            if not bot_token or not chat_id or "your_" in bot_token:
                logger.debug("Telegram not configured — skipping weekly review send")
                return False
            url = f"https://api.telegram.org/bot{bot_token}/sendMessage"
            payload = json.dumps({
                "chat_id":    chat_id,
                "text":       message,
                "parse_mode": "Markdown",
            }).encode("utf-8")
            req = urllib.request.Request(
                url, data=payload, headers={"Content-Type": "application/json"}
            )
            with urllib.request.urlopen(req, timeout=15) as resp:
                return resp.status == 200
        except Exception as exc:
            logger.warning("Telegram send failed: %s", exc)
            return False


